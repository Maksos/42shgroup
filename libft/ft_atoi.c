int		ft_atoi(char *s)
{
	int	i;
	int	ret;

	i = 0;
	ret = 0;
	while (s[i] &&
		(s[i] == ' ' || s[i] == '\n' || s[i] == '\f' ||
			s[i] == '\t' || s[i] == '\v' || s[i] == '\r'))
		i++;
	(s[i] && (s[i] == '-' || s[i] == '+')) ? (i += 1) : (i += 0);
	while (s[i] && s[i] >= '0' && s[i] <= '9')
	{
		if (ret != 0)
			ret = ret * 10;
		ret = ret + (s[i] - '0');
		i++;
	}
	i = 0;
	while (s[i] &&
		(s[i] == ' ' || s[i] == '\n' || s[i] == '\f' ||
			s[i] == '\t' || s[i] == '\v' || s[i] == '\r'))
		i++;
	if (s[i] && s[i] == '-')
		return (-ret);
	return (ret);
}
